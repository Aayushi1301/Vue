<!-- Using the heatmap component -->
<template>
  <div v-if="!this.loading" class="google-map" :id="mapName"></div>
</template>
<script>
 import axios from 'axios';
 export default {
  name: 'google-map',
  props: ['name'],
  data: function () {
    return {
      mapName: this.name + "-map",
       points:this.points,
       loading:false
    }
  },
  mounted: function () {
    const element = document.getElementById(this.mapName)
    const options = {
      zoom: 14,
      center: new google.maps.LatLng(48.0196,66.9237)
    }

    const map = new google.maps.Map(element, options);
    heatmap = new google.maps.visualization.HeatmapLayer({
                                            data: this.points,
                                            map: map
                                          });

  },
  methods:{
 getPoints() {
        return [
          points
          ];
}
  },
  created() {
          axios
            .get('heatmap/all')
            .then(response =>{

              //Iterating over the json data to get the valus of latitude and longitude
              //and assign it to points

              for(var i in response.data){
               this.points.push(new google.maps.LatLng(response[i].latitude,response[i].longitude));
              }






































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































            })
            .finally(() => this.loading = false)
  }
};

</script>
